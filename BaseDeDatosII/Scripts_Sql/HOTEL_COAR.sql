    DOMICILIO VARCHAR2(75) CONSTRAINT AGENCIA_COAR_DOMI_NN NOT NULL
);

SELECT * FROM AGENCIA_COAR;

-- TABLA DE RELACION AGENCIA_EMPLEADO_COAR
