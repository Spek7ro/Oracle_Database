-- TABLA EMPLEADO_COAR
CREATE TABLE EMPLEADO_COAR (
    ID_EMPLEADO NUMBER(6) CONSTRAINT EMPLEADO_COAR_PK PRIMARY KEY,
    APATERNO VARCHAR2(35) CONSTRAINT EMPLEADO_COAR_APA_NN NOT NULL,
    AMATERNO VARCHAR2(35),
    NOMBRE VARCHAR2(50) CONSTRAINT EMPLEADO_COAR_NOM_NN NOT NULL,
    CORREO VARCHAR2(25) CONSTRAINT EMP_COAR_CORREO_NN NOT NULL CONSTRAINT EMP_COAR_CORREO_UK UNIQUE,
    TELEFONO VARCHAR2(10) CONSTRAINT EMP_COAR_TELEFONO_NN NOT NULL, 
    DIRECCION  VARCHAR2(100) CONSTRAINT EMP_COAR_DIRECCION_NN NOT NULL,
    FECHA_CONTRATACION DATE CONSTRAINT EMPLEADO_COAR_FC_NN NOT NULL,
    SALARIO NUMBER(8,2) CONSTRAINT EMP_COAR_SALARIO_CK CHECK (SALARIO > 0)
);

SELECT * FROM EMPLEADO_COAR;

-- TABLA AGENCIA_COAR
CREATE TABLE AGENCIA_COAR (
    ID_AGENCIA NUMBER(6) CONSTRAINT AGENCIA_COAR_PK PRIMARY KEY,
    NOMBRE VARCHAR2(45) CONSTRAINT AGENCIA_COAR_NOMBRE_NN NOT NULL,
